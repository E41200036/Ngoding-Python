"""
JOIN TUPLE
----------
untuk melakukan [join] data tuple kamu bisa menggunakan operator [+]
"""

# contoh : 
num1 = tuple((1,2,3))
num2 = num1 + (4,5,6)
print(num2)

"""
MULTIPLY TUPLE
-------------
untuk melakukan perkalian jumlah data kamu bisa menggunakan operator [*]
"""

# contoh
num1 = (1,2,3)
num2 = 3 * num1
print(num2) # (1, 2, 3, 1, 2, 3, 1, 2, 3)
# nilai yang dihasilkan merupakan nilai dari jumlah perkalian tuple


